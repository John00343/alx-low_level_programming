#include <stdio.h>
#include <stdlib.h>

/**
 * add - add 2 int
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b) {
    return a + b;
}

/**
 * divide - divide 2 int
 * @a: first int
 * @b: second int
 * Return: division
 */
int divide(int a, int b) {
    return a / b;
}

/**
 * multiply - multiply 2 int
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int multiply(int a, int b) {
    return a * b;
}

/**
 * modulus - modulus of 2 int
 * @a: first int
 * @b: second int
 * Return: modulus
 */
int modulus(int a, int b) {
    return a % b;
}

/**
 * subtract - difference of 2 int
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int subtract(int a, int b) {
    return a - b;
}

