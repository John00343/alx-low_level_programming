#include <stdio.h>
#include <stdlib.h>

/**
 * add - add 2 int
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b) {
    return a + b;
}

/**
 * divide - divide 2 int
 * @a: first int
 * @b: second int
 * Return: division
 */
int divide(int a, int b) {
    return a / b;
}

/**
 * mul - multiply 2 int
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b) {
    return a * b;
}

/**
 * mod - modulus of 2 int
 * @a: first int
 * @b: second int
 * Return: modulus
 */
int mod(int a, int b) {
    return a % b;
}

/**
 * sub - difference of 2 int
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b) {
    return a - b;
}

